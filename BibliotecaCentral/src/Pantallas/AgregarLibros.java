    public void pintarComponentes(Libros l) {
        txtCodigo.setText(l.getCodigo());
        txtTitulo.setText(l.getTitulo());
        txtAutor.setText(l.getAutor());
        txtEditorial.setText(l.getEditorial());
        txtIsbn.setText(l.getISBN());
        txtEjemplares.setText(l.getNoEjemplares().toString());
    }
    
    public void mostrarDialogo() {
        modificar = false;
        Libros = new Libros();
        pintarComponentes(Libros);
        setVisible(true);
    }
    
    public void mostrarDialogo(Libros l) {
        modificar = true;
        Libros = l;
        pintarComponentes(l);
        setVisible(true);
    }
