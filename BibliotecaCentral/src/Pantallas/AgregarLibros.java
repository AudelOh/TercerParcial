    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Libros.setCodigo(txtCodigo.getText());
        Libros.setTitulo(txtTitulo.getText());
        Libros.setAutor(txtAutor.getText());
        Libros.setEditorial(txtEditorial.getText());
        Libros.setISBN(txtIsbn.getText());
        Libros.setNoEjemplares(Integer.parseInt(txtEjemplares.getText()));

        if (!modificar) {
            try {
                oController.agregar(Libros);

                this.setVisible(false);
                
            } catch (LibroExistenteException ex) {
                JOptionPane.showMessageDialog(this, "El producto ya ha sido registrado anteriormente", "Producto Existente", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

